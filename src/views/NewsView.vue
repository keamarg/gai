        <ol>
          <li>
            <strong>Hvordan GPT-4 dominerer AI-landskabet.</strong>OpenAI's
            GPT-4 model, grundlaget for ChatGPT, har været i fokus i det seneste
            år. GPT-4 er anerkendt som den mest modne og kraftfulde store
            sprogmodel på markedet. Den har demonstreret evnen til at bestå
            universitetsniveau-eksaminer inden for fag som jura og medicin ud
            over klassiske NLP-benchmarks.
          </li>
          <li>
            <strong
              >Hvordan RLHF har bevist sit værd og hvorfor det alligevel
              kritiseres kraftigt.</strong
            >
            GPT-4 er trænet ved hjælp af "reinforcement learning from human
            feedback" (RLHF), hvor menneskelige testere vurderer
            modelresultater. RLHF er afgørende for præstationen af brugervenlige
            chatbots som ChatGPT. Kritik er rejst angående de økonomiske og
            menneskelige omkostninger ved RLHF og beskæftigelsen af mange
            testere./li>
          </li>

          <li>
            <strong
              >Hvordan en ny tidsånd af mindre vidensdeling og gennemsigtighed
              vandt frem.</strong
            >
            Tidligere var offentliggørelse af forskningsresultater kutyme, men
            store kommercielle AI-selskaber som OpenAI og Google er blevet mere
            lukkede. Tekniske rapporter om ny AI-teknologi indeholder færre
            tekniske detaljer og øger hemmeligholdelsen. Lukkethed og tekniske
            rapporter med begrænset information er blevet normalen i
            AI-industrien.
          </li>
          <li>
            <strong
              >Kan Generativ AI's popularitet oversættes til økonomisk
              gevinst?</strong
            >
            Generative AI-produkter som ChatGPT oplever betydelig brugervækst,
            men det er uklart, om indtægterne følger med. Driftsomkostningerne
            for generative AI-produkter som ChatGPT er høje. Venturekapitalister
            investerer i AI-selskaber i USA og UK, mens investeringer i EU
            falder muligvis på grund af kommende AI-regulering.
          </li>
          <li>
            <strong>Chipefterspørgslen og produktion stiger dramatisk.</strong>
            Stigende investeringer i generativ AI øger behovet for GPU'er, som
            er afgørende for træning af moderne AI-modeller. NVIDIAs aktiekurs
            og omsætning er steget dramatisk på grund af stigende efterspørgsel
            efter GPU'er. Flere lande er involveret i en global kamp om kontrol
            over chipproduktion og eksport. Denne "chip-krig" er tydeligst
            imellem, men ikke begrænset til, USA og Kina.
          </li>
          <li>
            <strong
              >Forskelligartede AI-Reguleringer bliver lige nu udarbejdet og
              vedtaget over hele verden. </strong
            >Lande som Indien og England vedtager en "light-touch" tilgang til
            AI-regulering ved at bruge eksisterende lovgivning og kun indføre
            nye love, hvor det er nødvendigt. Kina og EU arbejder på omfattende
            AI-lovgivning baseret på samfunds- og risikoområder. USA og Canada
            tager en hybridtilgang og vedtager forskellige love på
            delstatsniveau og regulering i højrisikoindustrier.
          </li>
          <li>
            <strong
              >Sikkerhed og trusler Debatten om AI-bevidsthed og sikkerhed har
              fået meget opmærksomhed, men der er stadig stor usikkerhed omkring
              hvilke reelle trusler vi går i møde.</strong
            >
            Både politikere og eksperter tager spørgsmålet om AI-sikkerhed
            alvorligt. En AI Safety Summit planlægges lige nu i London for at
            diskutere sikkerhedsaspekterne ved AI og mødes med verdens ledere og
            eksperter på området.
          </li>
        </ol>
